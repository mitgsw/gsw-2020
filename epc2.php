    <h2>About</h2>

    <p>Ludico is a fun vocabulary-building app.

    </p><p>Everyday, when you open the app, you earn a new word (introduced with a quote, a fun fact or a game) that is added to your collection. The collection contains definitions, synonyms and etymology. You also earn virtual money to help you buy lives (to play more) or bonuses. There is an exclusive mini-game around words that changes every day: anagrams, dingbats, form the longest word, crosswords, etc.

    </p><p>You can play alone (link the synonyms; multiple-choice quiz: You see a definition, and you have to pick the right word among four choices; text with a missing word that you must choose among choices) or with other people (Quiz, Hangman Game, Dictionary Game: One player has the correct definition of a difficult word, and the three other users need to come up with a definition then everyone votes for the definition they believe to be correct). You can also interact with your friends and challenge them.

    </p><p>For the solo mode, you have a predefined path with levels you must follow. When you complete a level, you earn words that are added to your collection. When you fail, you lose a life, and when you have no lives left, you must wait to play again (unless you buy a life). Each month you must replay with previous words so that you can practice and memorize them.</p>
    
    <h5>What is the main goal of your startup? What do you hope to achieve in France and/or the rest of the world?</h5>

    <p>Help save the French language! I do believe the situation is alarming. Language is becoming impoverished. People do not read as much as before. The right words are no longer being used, or they are used in the wrong context. Commonplaces are multiplying.

    </p><p>I think it is an issue worth tackling. If you know vocabulary, you can express what you really think; moreover, your thoughts are actually sharper because you can grasp nuances. Of course, you can express yourself with a limited vocabulary, but I firmly believe words matter. The right words at the right moment can change your life.

    </p><p>After French, I would like to explore other languages too!</p>

    <h5>Where did you get the inspiration for your idea?</h5>

    <p>My dream is to create a project in the field of education. I have always been fascinated by words such as 'zeugma' (a neat figure of speech) and 'paroxystique.'

    </p><p>At GEM, I had the opportunity to pitch a project. But I had no idea.

    </p><p>Three days before the pitch session, before going to bed, I was reading when I came across a word I didn't know. I told myself, "This word is really interesting, but I will never remember it. Wouldn't it be cool if I could create an app to discover new words and retain them?" and that was it.

    </p><p>Three authors definitely had an influence on me: In My Father's Glory, Marcel Pagnol recalls how he would write down all the words he liked in a booklet when he was young.

    </p><p>In 1984, George Orwell describes Newspeak, a language created by the totalitarian regime, which contains only the essential words and nothing more. Because the more you reduce the number of words, the more you shrink your thoughts.

    </p><p>In Wind, Sand and Stars, Antoine de Saint Exupéry travels by train, in third class with Polish refugees. He sees a child full of promise who reminds him of Mozart but who will never be able to develop his talents. He has this thought, "What torments me is not misery, it is in each of these men Mozart murdered." For me, it evokes young students who won't reach their potential because they lack vocabulary: In each of them, it is Molière who is murdered.</p>

    <h5>What is your proudest startup accomplishment to date?</h5>

    <p>
    Teaching the meaning of 'pusillanime' (which means being afraid of taking risks and responsibilities) to most of the people I interviewed.

    </p><p>I use a quote from Napoléon to illustrate it: "In all that you undertake, two-thirds must be given to reason, and the other third to chance. Increase the first fraction, and you'll be pusillanimous. Increase the second, and you will be foolhardy."

    </p><p>Interviewees tell me they are happy to learn something!

    </p><p>Two people already knew the meaning of 'pusillanime,' so I tried 'chryséléphantin' (which means made of gold and ivory, for example 'une statue chryséléphantine'). It worked.</p>

    <h5>What are the biggest challenges you anticipate in the future?</h5>

    <p>Finding a business model is the biggest hurdle I can anticipate. I thought of advertising, freemium and in-app purchases, but whether it is profitable or not remains to be seen. My dream would be finding a philanthropist akin to Cyrano de Bergerac: "Rien ne me reste. Mais quel geste !" ('I’ve nothing further. But what a gesture!')

    </p><p>Retention is another concern: Can I build something users will find so enjoyable and valuable that they will keep using it every day, enriching their collection while having fun?</p>

    <h5>What do you plan to do next?</h5>

    <p>Keep learning new words, designing games and interviewing people. Feel free to get in touch if you are inspired by the project!

    </p><p>An important next step is to create a prototype for further tests. After that, hopefully I will be able to launch a complete version next year.</p>

