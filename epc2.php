    </p><p>Finally, it is really encouraging to have been selected as one of the winners. Now my mom is starting to believe in the project! Jokes aside, it meant a lot for me both personally (two years ago I wouldn't even have dared to apply) and professionally.

    </p><p>MIT GSW will definitely help me move forward. Thank you very much!</p>
