    <h5>What are the biggest challenges you anticipate in the future?</h5>

    <p>1) Develop performant algorithms: Our technology requires very fast and accurate decision making in order to provide a safe assistance experience when planning trajectories, thus requiring high computing power and energy. In contrast, our device must be light in weight and handy in order to be adopted by blind people.
    </p><p>2) Embedded navigation systems must predict the movement of surrounding moving objects to anticipate its next move and update trajectories and path plan real time.
    </p><p>3) Data Collection: There are not many datasets currently that classify objects on sideways, sideways, metro stations, airports, building entrances, etc.
    </p>

