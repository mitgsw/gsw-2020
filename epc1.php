    <h2>About</h2>

    <p>Seeingrobot addresses a problem that impacts more than 250 million people around the world: autonomy in mobility for both partially and completely visually impaired people. MiMi, its first innovation, is an assistive technology that aims to bring back autonomy for VI's in their day-to-day mobility. It integrates several cutting-edge technologies, such as robotics and deep learning, to solve four principal challenges: a) going to unknown places while having a sense of the surrounding environment; b) detecting and avoiding obstacles; c) detecting and recognizing guiding signs; and d) moving in full security and comfort.
    </p>
    
    <h5>What is the main goal of your startup? What do you hope to achieve in France and/or the rest of the world?</h5>

    <p>Seeingrobot's main goal is to improve mobility and transport through the use of technology. In France, we hope to develop our first autonomous assistive technology for blind people ASAP in order to give access to more than 1.7M people. In the future, our goal is to roll it out to North America, Europe and APAC, and then to the rest of the world. We see potential spin-offs from our concept and are looking towards tomorrow very optmistically.
    </p>

    <h5>Where did you get the inspiration for your idea?</h5>

    <p>Rida, the Founder and CEO of Seeingrobot, comes from a family where his two grandparents lost their vision with age. He witnessed how they suffered in their day-to-day life, especially in mobility and always wanted to find a solution for them. Today with the reduced costs of components and sensors, and the rise of cloud computing, big data and AI, autonomous vehicles and drones became a reality. The question is, why not learn from these advancement and bring them to the handicap world, and help them get included in the society again and change their life?
     </p>

    <h5>What is your proudest startup accomplishment to date?</h5>

