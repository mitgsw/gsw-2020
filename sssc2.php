    <h2>About</h2>

    <p>Lancey is pioneering a new era for energy storage, an era in which home appliances can strongly support energy transition by storing electricity at home and engaging citizens to become active prosumers while reducing their electricity bill. 

    </p><p>We aim to bring a qualitative electricity storage device in every home by integrating it into a space heater and sharing most of the electronic parts in a way that divides the storage cost by two. Our first product is the first electric smart heater with an embedded battery and energy management system. 

    </p><p>Why is it a solution for a sustainable society? 

    </p><p>    With a CAPEX up to 3 times lower than a gas boiler/heat pump, it brings a solution for the millions of households in fuel poverty.  

    </p><p>    It participates in the energy transition by facilitating the domestic self-consumption of local renewable energies. 

    </p><p>    The central management of the batteries is key to enable a smart grid. 

    </p><p>    Our products will soon integrate a second-life battery from e-bikes. 

    </p><p>Lancey is a 3-year-old company and relies on a young and enthusiastic team of 30 people based in Grenoble (Fr) and Montréal (Ca). Lancey Energy Storage received financial support from the EasyTech program of the IRT Nanoelec in 2019. Two communications modules (Wi-FI and KNX) were developed in the frame of this cooperation. 

    </p><p>We have already installed 1,000 units in France, with 10 B2B clients in different regions of France and in different conditions (house/flat, new/renovation, with or without solar panels, tertiary building VS residential). The solution has proven significant energy bill reductions (up 50%), with a quick return on investment. 
    </p>
    
    <h5>What is the main goal of your startup? What do you hope to achieve in France and/or the rest of the world?</h5>

    <p>Our goal is to deploy photovoltaics panel on top of every single house and to increase the efficiency of the main domestic uses of energy.  

    </p><p>To achieve this deployment, we will propose a complete set of solutions allowing the management of the photovoltaics production in order to simplify the self-consumption and the local valorisation of this green and sustainable electricity.  

    </p><p>We act on both sides of energy consumption to reduce greenhouse gas emissions: 
    <ul>
    <li></p><p> Our AI programming of the heat generation allows us to reduce the final consumption of energy by 30% (automatic turn-off of the radiator as a function of user habits).
</li>
<li></p><p>The battery provides electricity for heating and other appliances during the peak period (where electricity has the highest carbon content).
</li>
    </ul>

</p><p>On the other hand, we actively participate in the energy transition by tremendously facilitating the domestic self-consumption of renewable energies (e.g. from photovoltaics solar panels). Thus, our standard configuration of radiator (1 kWh of battery storage per radiator) allows a 100% of self-consumption ratio for France, the U.K., Germany, Benelux, Canada, etc. 

</p><p>Finally, the storage capacity of Lancey participates in the active equilibrium of the electric grid, which requires additional capacity to push the renewable energy content up to 100%. 
</p>

    <h5>Where did you get the inspiration for your idea?</h5>

    <p>Lancey’s project comes from Raphaël Meyer’s idea of combining a battery with an electric radiator to democratize batteries in France in order to foster the energy transition. Raphaël presented this idea for the first time in 2014 and soon met Gilles Moreau, a battery specialist leading an R&D project in this field. In 2015, they joined forces with Hervé Ory, a serial entrepreneur who had already created several successful companies. After applying for the first patents of the company, they create Lancey Energy Storage in 2016.   

    </p><p>We named the company Lancey after a small village near Grenoble where hydroelectric energy has been invented by Aristide Bergès at the beginning of the previous century. So far, this is a simplest and greenest energy ever produced. One of our main drivers is therefore "Energy transition for all."
</p>

    <h5>What is your proudest startup accomplishment to date?</h5>

    <p>
    We are especially proud of having deployed our first solution (i.e. one of the most advanced heating systems in the world) into social landlord's dwellings. It is quite logical as we want it to become a universal product, everywhere and beneficial to all. But this pretty challenging proof of concept has also allowed to avoid technological biases, which can be met with tech-friendly early adopters.  

    </p><p>Based on our return on experiments, we could convince the jury of the CTA Association (organizing the CES in Las Vegas) to award us with a Best of Innovation in 2018. It provided us with great international exposure for our solutions, but also for this strong obstacle to energy transition, which is the fuel poverty. 
</p>

