                <p>
                The research team from Grenoble Ecole de Management (GEM) dedicated to innovation management has launched 
                a cooperation with The French Alternative Energies and Atomic Energy Commission (CEA). Collaborating with 
                one of the CEA labs (Leti- Institut CEA Tech), they identified 75 technologies (especially nanotechnologies) 
                developed by the lab: some quite well known, such as accelerometer or rfid, and some more futuristic, such as
                 bioprinting or brain-computer interfaces. How can we imagine new products and services using these technologies? 
                 To do this, they integrated the technologies into a real board game called Tech it! By playing the game, p
                 articipants innovate and build ideas that could be then discussed and implemented with the lab researchers. </p>
 
                <p>During the workshop, the participants will play a “Deep Tech challenge,” using a portfolio of “Deep Tech” 
                to imagine products and services to improve health and well-being. By doing this, they will better understand 
                the nature of these technologies and their potential uses and impact on society. They will pitch their ideas 
                and have experts’ feedback.
                </p>
                <h6 style="display: inline">Objective:</h6> <p style="display: inline">Participants will understand the potential of Deep Tech while 
                experimenting with an original ideation method to generate business ideas.</p>

