                <p> Despite the large amount of information available these days, becoming an entrepreneur and creating a new startup 
                is still an uncertain path. Too much advice is given to entrepreneurs without them really ever understanding where it 
                comes from. Or, too many actions are taken by founders simply because it's "what others do". In this workshop, 
                Chris Howard takes us back to fundamentals - but in a way rarely explored before. Understanding these fundamentals - 
                asking the "dumb" questions - allows you to create your own techniques, take your own advice, and intentionally 
                do what others *don't* do. Chris will share his insights and experiences as a serial entrepreneur, entering the 
                world of entrepreneurship from an original perspective and sharing what no one dares to speak.</p>
