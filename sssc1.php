    <h2>About</h2>

    <p>At BeFC, we make electricity from paper and enzymes. Our paper-based biofuel cells offer a sustainable and environmentally-friendly energy solution for low power electronics. There is a growing trend towards wearable and single-use electronics (e.g., wearable continuous glucose monitors for diabetes, point-of-care medical tests, and connected packaging). However, the problem is that most of these devices are powered by lithium coin- or button-cell batteries. For all miniature battery chemistries, an average of 97% end up in landfill, polluting our planet.

</p><p>We invented our paper bioenzymatic fuel cells as a sustainable and ecofriendly energy solution for low power electronics. In contrast to conventional batteries, our devices are bio-sourced and are metal-free. We use enzymes to convert glucose and oxygen into electricity. And as our devices are made from paper, they are ultra-thin and flexible, perfect for wearable devices.

</p><p>BeFC is already working with several major companies that are interested in reducing both their environmental impact, as well as their recycling and disposal costs. We are now seeking industrials partners for product development and looking to raise investment, in order to scale production.</p>
    
    <h5>What is the main goal of your startup? What do you hope to achieve in France and/or the rest of the world?</h5>

    <p>The main goal of BeFC is simple: provide a sustainable and ecofriendly alternative to conventional batteries. 

</p><p>We aim to reach a point where the status-quo is to use a BeFC product for wearable and single-use applications, for both new and existing applications. Our first facility is located in the heart of the Alps in Grenoble, where we have a superb network to support and accelerate the development of innovative technology. We have an ambitious vision for our future and would like to set up production facilities in strategic locations to help service our clients. For instance, we are planning a production facility in the USA for the end of 2023.</p>

    <h5>Where did you get the inspiration for your idea?</h5>

    <p>Our technology is bio-inspired. Just like the body uses enzymes to convert biofuels to energy, our paper-based biofuel cells use enzymes to convert glucose and oxygen to electricity.

    </p><p>As a spin-out of CNRS, the National Centre of Scientific Research, we have a strong academic background. The team at Grenoble are experts on molecular electrochemistry and bioelectrochemistry.

    </p><p>The original concept was to use the technology in an implantable format to power devices such as pacemakers. However, we decided to pivot towards providing a sustainable solution for the emerging flexible and wearable electronics markets.

    </p><p>As seen so often with interdisciplinary teams, by bringing together experts in different fields of science, we were able to make significant improvements in the technology and its implementation towards a commercially viable product. </p>

    <h5>What is your proudest startup accomplishment to date?</h5>

    <p>
    Attending the Consumer Electronics Show (CES) 2020 in Las Vegas has been a major highlight for many of us at BeFC so far this year. CES 2020 is a major milestone for many startups in the technology space, and allowed us to showcase our disruptive technology to the world.

    </p><p>We were overwhelmed by the positivity and enthusiasm of the public and professionals in attendance, who share our vision for a sustainable future.  
    </p>

    <h5>What are the biggest challenges you anticipate in the future?</h5>

    <p>The biggest challenge for the next few years will be scaling our production capability, while maintaining our stringent quality procedures. Whilst we have brought on talented team members with experience in paper-based production in roll-to-roll format, we understand that successful implementation will be key to ensuring that this technology can reach the general public.

    </p><p>BeFC will also require the support of major manufacturers in the industry that are willing to become early adopters of our paper-based biofuel cell technology. We hope that companies can be forward-thinking and be willing to work alongside us to develop the next generation of sustainable and eco-responsible products. 
    </p>

    <h5>What do you plan to do next?</h5>

    <p>BeFC is actively raising investment to accelerate the development of our technology and production scale-up. Our goal is to be able to produce up to 80,000 units per day by the start of 2022 by designing and commissioning our first production lines. Careful engineering of the device and its production line help to position our pricing to be on par, or below that, of conventional batteries.

    </p><p>We are already on the look for talented and ambitious individuals willing to help the team achieve our vision. The company hopes to rapidly increase our team in the next two years whilst continuing to build strong relationships with our early customers.</p>

    <h5>How has MIT GSW helped you in advancing in your goals?</h5>

    <p>The MIT Global Startup Workshop has identified innovative international startups for over 20 years and has been endorsed by world class leaders. BeFC hopes that such a prestigious competition will increase awareness of our technology and provide future opportunities for growth, particularly with the recruitment of talented individuals.</p>
